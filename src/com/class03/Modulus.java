package com.Class03;

public class Modulus {

	public static void main(String[] args) {
		
		int a=10;
		int b=3;
		
		int div=a/b;
		
		System.out.println(div);//3 or 3.3
		
		//Modulus operator also known as the % percentage sign
	
		int mod=a%b;
		
		System.out.println(mod);//Modulus operator gives us the remainder of the number in a division
		
		int y=20+-3*5/8;
		int z=5+15/3*2-8%3;
		
		System.out.println(y);
		System.out.println(z);

	}

}



package com.class02;

public class Variabledeclaration {

	public static void main(String[] args) {
		//different ways to create/declare variables;
		//1.create variable and assign value
		int num=25;
		
		//2 way: using 2 steps
		int a; //1. declare variable
		a=10; //2. then assign value
		
		//declare 1 by 1 and assign the value later
		int b;
		int c;
		b=40;
		c=50;
		
				
		//or declare all together by adding commas(,)
		int f,g;
		f=20;
		g=30;
		
		//everytime assigning value or variables, state it line by line
		
		//for windows, use ctrl_shift+f = formating 
		//for mac, cmd+shift+f = formating
				
				System.out.println(num);
				System.out.println(a);
				System.out.println(b);
				System.out.println(c);
				System.out.println(f);
				System.out.println(g);
				
	}

}
